// ============= OUTFIT MATRIX =============
// Internal-only expanded library with clustering for intelligent selection
// Each outfit is tagged with multiple attributes for precise matching

// ============= CLUSTER TYPES =============
export type SkinToneFit = 'cool' | 'warm' | 'neutral';
export type ContrastFit = 'low' | 'medium' | 'high';
export type BodyTypeFit = 'slim' | 'athletic' | 'broader';
export type TasteFit = 'minimalist' | 'classic' | 'elevated';
export type VibeFit = 'safe' | 'sharp' | 'relaxed';

export type TierType = 'SAFEST' | 'SHARPER' | 'RELAXED';

// ============= ENHANCED OUTFIT ENTRY =============
export interface MatrixOutfitEntry {
  id: string;
  occasion: OccasionType;
  tier: TierType;
  
  // Context filters
  location: LocationType[];
  time: TimeType[];
  setting: SettingType[];
  budget: BudgetType[];
  
  // Cluster attributes (internal selection)
  skin_tone_fit: SkinToneFit[];
  contrast_fit: ContrastFit[];
  body_type_fit: BodyTypeFit[];
  taste_fit: TasteFit[];
  vibe: VibeFit;
  
  // Formality and confidence
  formality_level: 1 | 2 | 3 | 4 | 5; // 1=casual, 5=formal
  confidence_score: number; // 1-10, higher = more universally flattering
  
  // Visual details
  palette: string[]; // Colors used (for visual consistency check)
  image_url: string;
  
  // Content
  title: string;
  items: {
    top: string;
    bottom: string;
    shoes: string;
    extras?: string;
  };
  reason: string;
}

