// ============= MATRIX UTILITIES =============

// Get all valid matrix outfits
export function getValidMatrixOutfits(): MatrixOutfitEntry[] {
  return OUTFIT_MATRIX.filter(outfit => 
    outfit.image_url && 
    outfit.items?.top && 
    outfit.items?.bottom && 
    outfit.items?.shoes &&
    outfit.title &&
    outfit.reason &&
    outfit.occasion &&
    outfit.tier
  );
}

// Get outfits by occasion
export function getMatrixOutfitsByOccasion(occasion: OccasionType): MatrixOutfitEntry[] {
  return getValidMatrixOutfits().filter(outfit => outfit.occasion === occasion);
}

// Get outfits by tier
export function getMatrixOutfitsByTier(tier: TierType): MatrixOutfitEntry[] {
  return getValidMatrixOutfits().filter(outfit => outfit.tier === tier);
}
